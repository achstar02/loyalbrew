let currentLang = localStorage.getItem('loyalbrew_lang') || 'en';

function t(key) {
  const dict = LANGS[currentLang] || LANGS['en'];
  const val = dict[key];
  if (val === undefined) return LANGS['en'][key] ?? key;
  // Support {v} and {n} placeholders
  return String(val);
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('loyalbrew_lang', lang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  // Update lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.style.background = btn.id === 'lang-' + lang
      ? 'rgba(255,255,255,0.25)'
      : 'transparent';
  });
}
