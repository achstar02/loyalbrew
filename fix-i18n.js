function buildLangString(lang) {
  const t = translations[lang];
  const entries = Object.entries(t);
  const lines = [];
  for (let i = 0; i < entries.length; i += 6) {
    const chunk = entries.slice(i, i + 6);
    const line = chunk.map(([k, v]) => `    ${k}: '${v}'`).join(', ');
    lines.push(line + (i + 6 < entries.length ? ',' : ''));
  }
  return lines.join('\n');
}

// For each language block in first LANGS object, insert before the closing }, 
// We need to find the unique marker for each block's end

const langInsertMarkers = {
  en: "mDaySat: '?',\n  },",
  zh: "mDaySat: '??',\n  },",
  ms: "mPhComplaintOrderId: 'cth: ORD123456',\n  },",
  ta: "mPhComplaintOrderId: '??:ORD123456',\n  },"
};

let changes = 0;
for (const [lang, marker] of Object.entries(langInsertMarkers)) {
  if (content.includes(marker)) {
    const insert = buildLangString(lang);
    const replacement = insert + '\n  },';
    content = content.replace(marker, replacement);
    console.log(`✅ Inserted ${Object.keys(translations[lang]).length} keys into LANGS.${lang}`);
    changes++;
  } else {
    // Try alternate markers
    console.log(`⚠️ Marker not found for ${lang}, trying fallback...`);
  }
}

